package Actividad4;

public class Variables {
    public static void main(String[] args) {

        int numeroA = 89;
        int numeroB = 323;

        System.out.println(numeroA);
        System.out.println(numeroB);

        System.out.println("-----------------");

        System.out.println(numeroA + numeroB);
        System.out.println(numeroA - numeroB);
        System.out.println((double)numeroA / (double)numeroB);
        System.out.println(numeroA * numeroB);

    }
}